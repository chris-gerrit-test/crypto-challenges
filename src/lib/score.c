double score_text(char* text, size_t len) {
	double score = 0.0;
	char c;
	for (int i = 0; i < len; ++i) {
		c = text[i];
		score += letter_scores[(unsigned char)tolower(c)];
	}
	return len == 0 ? 0.0 : score / len;
}

int hamming_distance(char* str1, char* str2) {
	int d = 0;
	while (*str1) {
		char diff = *str1 ^ *str2;
		d += __builtin_popcount(diff);
		++str1;
		++str2;
	}
	return d;
}
